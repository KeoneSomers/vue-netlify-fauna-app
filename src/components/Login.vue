<template>
  <div class="login-page">
    <div id="netlify-auth"
      @click="openNetlifyAuth" > open netlify auth </div>
    <div class="form">
      
      <form v-if="mode === 'register'" class="register-form stack">
        <h2>👋 Register Here</h2>
        <input type="text" placeholder="name" v-model="crendentials.name" />
        <input type="text" placeholder="email address" v-model="crendentials.email" />
        <input type="password" placeholder="password" v-model="crendentials.password" />
        <button type="button" @click="signup()">Sign Up</button>
        <p class="message">
          Already registered?
          <a @click="toggleMode" href="#">Sign In</a>
        </p>
      </form>

      <form v-else class="login-form stack">
        <h2>🔐 Login Here</h2>
        <input type="text" placeholder="username" v-model="crendentials.email" />
        <input type="password" placeholder="password" v-model="crendentials.password" />
        <button type="button" @click="login()">login</button>
        <p class="message">
          Not registered?
          <a @click="toggleMode" href="#">Create an account</a>
        </p>
      </form>

    </div>
  </div>


</template>

<script>
import { mapActions } from "vuex";
import netlifyIdentity from "netlify-identity-widget"

export default {
  name: "Login",
  data() {
    return {
      crendentials: {
        name: "",
        password: "",
        email: ""
      },

      mode: "login"
    };
  },
  methods: {
    ...mapActions("auth", ["attemptLogin", "attemptSignup"]),
    toggleMode() {
      if (this.mode === "register") {
        this.mode = "login";
      } else {
        this.mode = "register";
      }
    },
    signup() {
      this.attemptSignup(this.crendentials)
        .then((response) => {
          alert(`Confirmation email has been sent to you!
                 ${response.id}`);
          console.log(response)
        })
        .catch(error =>{
          alert(`Somethings gone wrong signing up.
                 Error: ${error}`)
          console.error(error, "Somethings gone wrong signing up")
        });
    },
    login() {

      let token = decodeURIComponent(window.location.search).substring(1).split("confirmation_token=")[1];

      this.attemptLogin({token , ...this.crendentials})
        .then(() => {
          alert(`You have signed in!`);
          this.$router.push(this.$route.query.redirect || "/");
        })
        .catch(error => {
          alert(`Somethings gone wrong logging up.
                 Error: ${error}`)
          console.error(error, "Somethings gone wrong logging in")
        });
    },

    openNetlifyAuth(){
      netlifyIdentity.open();
    }
  },
  mounted () {
    netlifyIdentity.init({
      container: '#netlify-auth' // defaults to document.body,
    });

    netlifyIdentity.on('init', user => {
      console.log('init', user)
      });
    netlifyIdentity.on('login', user => {
      console.log('login', user)
        let token = decodeURIComponent(window.location.search).substring(1).split("confirmation_token=")[1];

      this.attemptLogin({token , ...this.crendentials})
        .then(() => {
          alert(`You have signed in!`);
          this.$router.push(this.$route.query.redirect || "/");
        })
        .catch(error => {
          alert(`Somethings gone wrong logging up.
                 Error: ${error}`)
          console.error(error, "Somethings gone wrong logging in")
        });
      });
    netlifyIdentity.on('logout', () => console.log('Logged out'));
  },
};
</script>

